package primeiro;

public class Marca {

	private String marca;
	private int nrDeModelos;
	private String nomeMarca;
	private int anoLancamento;
	private int codIdentificador;
	
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public int getNrDeModelos() {
		return nrDeModelos;
	}
	public void setNrDeModelos(int nrDeModelos) {
		this.nrDeModelos = nrDeModelos;
	}
	public String getNomeMarca() {
		return nomeMarca;
	}
	public void setNomeMarca(String nomeMarca) {
		this.nomeMarca = nomeMarca;
	}
	public int getAnoLancamento() {
		return anoLancamento;
	}
	public void setAnoLancamento(int anoLancamento) {
		this.anoLancamento = anoLancamento;
	}
	public int getCodIdentificador() {
		return codIdentificador;
	}
	public void setCodIdentificador(int codIdentificador) {
		this.codIdentificador = codIdentificador;
	}

}
